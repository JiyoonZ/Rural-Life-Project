package com.project.rural.worker;

public class WorkApplyDTO {
	private String id;
	private String seq;
	private String pseq;
	private String totalApply;
	private String detail;
	private String isPass;
	private String regDate;
	private String isCareer;
	
	private String name;
	private String age;
	private String gender;
	private String tel;
	private String birth;
	
	public String getBirth() {
		return birth;
	}
	public void setBirth(String birth) {
		this.birth = birth;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getPseq() {
		return pseq;
	}
	public void setPseq(String pseq) {
		this.pseq = pseq;
	}
	public String getTotalApply() {
		return totalApply;
	}
	public void setTotalApply(String totalApply) {
		this.totalApply = totalApply;
	}
	public String getDetail() {
		return detail;
	}
	public void setDetail(String detail) {
		this.detail = detail;
	}
	public String getIsPass() {
		return isPass;
	}
	public void setIsPass(String isPass) {
		this.isPass = isPass;
	}
	public String getRegDate() {
		return regDate;
	}
	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getIsCareer() {
		return isCareer;
	}
	public void setIsCareer(String isCareer) {
		this.isCareer = isCareer;
	}
	
}
